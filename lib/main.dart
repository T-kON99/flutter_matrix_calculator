class MyApp extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    final test = Matrix(data: [[1, 2], [3, 1]]);
    final other = Matrix(data: [[3, 2], [2, 4]]);
    print((test*other).data);
    return MaterialApp(
      title: 'Welcome to Flutter',
      home: Scaffold(
